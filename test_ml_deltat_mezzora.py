import numpy as np
import pandas as pd
import matplotlib.pyplot as plt

from sklearn.metrics import mean_absolute_error, mean_squared_error, r2_score
from sklearn.pipeline import Pipeline
from sklearn.preprocessing import StandardScaler
from sklearn.linear_model import LinearRegression
from sklearn.ensemble import RandomForestRegressor
from sklearn.neural_network import MLPRegressor
import io

df = pd.read_csv("dataset_ML.csv")
df.head()

# conversione tempo e ordinamento
df["time"] = pd.to_datetime(df["time"])
df = df.sort_values("time").reset_index(drop=True)

print(df.head())
print(df.shape)

print("Shape:", df.shape)
print("Colonne:", df.columns.tolist())
print("\nInfo dataset:")
print(df.info())

if "timestamp" in df.columns:
    df = df.rename(columns={"timestamp": "time"})

df["time"] = pd.to_datetime(df["time"], utc=True)
df = df.sort_values("time").reset_index(drop=True)

df.head()

print(df.isna().sum())

df["delta_min"] = df["time"].diff().dt.total_seconds() / 60

print(df[["time", "delta_min"]].head(10))
print("\nDistribuzione delta_min:")
print(df["delta_min"].value_counts(dropna=False).sort_index())

allowed_steps = {5, 10, 15}

df["delta_min_rounded"] = df["delta_min"].round().fillna(5)
df["is_gap"] = ~df["delta_min_rounded"].isin(allowed_steps)

print(df[["time", "delta_min", "delta_min_rounded", "is_gap"]].head(15))

df["segmentid"] = df["is_gap"].cumsum()

segmentsizes = (
    df.groupby("segmentid")
      .size()
      .reset_index(name="length")
      .sort_values("length", ascending=False)
      .reset_index(drop=True)
)

print(segmentsizes.head(20))
print("\nNumero segmenti:", df["segmentid"].nunique())

windowsize = 30
horizon = 6              # 6 step da 5 minuti = 30 minuti
minlen = windowsize + horizon

validsegments = segmentsizes.loc[segmentsizes["length"] >= minlen, "segmentid"]

dfvalid = df[df["segmentid"].isin(validsegments)].copy().reset_index(drop=True)

print("Shape dfvalid:", dfvalid.shape)
print("Segmenti validi:", dfvalid["segmentid"].nunique())
print("Lunghezza minima richiesta:", minlen)

featurecols = ["temp", "hum", "lux"]

X_seq = []
last_temp_list = []
future_temp_list = []
y_delta = []
metatime = []

for segid, segdf in dfvalid.groupby("segmentid"):
    segdf = segdf.sort_values("time").reset_index(drop=True)

    values = segdf[featurecols].values
    temps = segdf["temp"].values
    times = segdf["time"].values

    for i in range(len(segdf) - windowsize - horizon + 1):
        x_window = values[i:i + windowsize]
        last_temp = temps[i + windowsize - 1]
        future_temp = temps[i + windowsize + horizon - 1]
        delta_t = future_temp - last_temp

        X_seq.append(x_window)
        last_temp_list.append(last_temp)
        future_temp_list.append(future_temp)
        y_delta.append(delta_t)
        metatime.append(times[i + windowsize + horizon - 1])

X_seq = np.array(X_seq)
last_temp_arr = np.array(last_temp_list)
future_temp_arr = np.array(future_temp_list)
y_delta = np.array(y_delta)
metatime = np.array(metatime)

print("Shape X_seq:", X_seq.shape)
print("Shape y_delta:", y_delta.shape)
print("Prime 5 delta:", y_delta[:5])

Xflat = X_seq.reshape(X_seq.shape[0], X_seq.shape[1] * X_seq.shape[2])

print("Shape Xflat:", Xflat.shape)

splitidx = int(len(Xflat) * 0.8)

Xtrain = Xflat[:splitidx]
Xtest = Xflat[splitidx:]

Xtrain_seq = X_seq[:splitidx]
Xtest_seq = X_seq[splitidx:]

ytrain_delta = y_delta[:splitidx]
ytest_delta = y_delta[splitidx:]

last_temp_train = last_temp_arr[:splitidx]
last_temp_test = last_temp_arr[splitidx:]

future_temp_train = future_temp_arr[:splitidx]
future_temp_test = future_temp_arr[splitidx:]

timetrain = metatime[:splitidx]
timetest = metatime[splitidx:]

print("Train classici:", Xtrain.shape, ytrain_delta.shape)
print("Test classici :", Xtest.shape, ytest_delta.shape)
print("Train seq     :", Xtrain_seq.shape, ytrain_delta.shape)
print("Test seq      :", Xtest_seq.shape, ytest_delta.shape)

# =========================================================
# metriche di valutazione
# =========================================================

def regression_metrics(y_true, y_pred):
    mae = mean_absolute_error(y_true, y_pred)
    rmse = np.sqrt(mean_squared_error(y_true, y_pred))
    r2 = r2_score(y_true, y_pred)
    return mae, rmse, r2

# =========================================================
# Baseline naive sul delta
# =========================================================

naive_delta_pred = np.zeros_like(ytest_delta)
naive_temp_pred = last_temp_test + naive_delta_pred

naive_mae, naive_rmse, naive_r2 = regression_metrics(future_temp_test, naive_temp_pred)

print("Naive baseline (delta = 0)")
print("MAE :", naive_mae)
print("RMSE:", naive_rmse)
print("R2  :", naive_r2)

# =========================================================
# Linear Regression sul delta
# =========================================================

lin_model = Pipeline([
    ("scaler", StandardScaler()),
    ("model", LinearRegression())
])

lin_model.fit(Xtrain, ytrain_delta)
lin_delta_pred = lin_model.predict(Xtest)
lin_temp_pred = last_temp_test + lin_delta_pred

lin_mae, lin_rmse, lin_r2 = regression_metrics(future_temp_test, lin_temp_pred)

print("Linear Regression (delta)")
print("MAE :", lin_mae)
print("RMSE:", lin_rmse)
print("R2  :", lin_r2)

