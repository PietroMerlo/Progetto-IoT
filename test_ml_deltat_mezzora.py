import tensorflow as tf
from tensorflow.keras.models import Sequential
from tensorflow.keras.layers import Input, LSTM, Dense, Dropout
from tensorflow.keras.callbacks import EarlyStopping

n_features = Xtrain_seq.shape[2]

x_scaler = StandardScaler()
Xtrain_2d = Xtrain_seq.reshape(-1, n_features)
Xtest_2d = Xtest_seq.reshape(-1, n_features)

Xtrain_scaled = x_scaler.fit_transform(Xtrain_2d).reshape(Xtrain_seq.shape)
Xtest_scaled = x_scaler.transform(Xtest_2d).reshape(Xtest_seq.shape)

y_scaler = StandardScaler()
ytrain_delta_scaled = y_scaler.fit_transform(ytrain_delta.reshape(-1, 1)).ravel()
ytest_delta_scaled = y_scaler.transform(ytest_delta.reshape(-1, 1)).ravel()

tf.random.set_seed(42)
np.random.seed(42)

lstm_model = Sequential([
    Input(shape=(Xtrain_scaled.shape[1], Xtrain_scaled.shape[2])),
    LSTM(64),
    Dropout(0.2),
    Dense(32, activation="relu"),
    Dense(1)
])

lstm_model.compile(
    optimizer="adam",
    loss="mse",
    metrics=["mae"]
)

early_stop = EarlyStopping(
    monitor="val_loss",
    patience=15,
    restore_best_weights=True
)

history = lstm_model.fit(
    Xtrain_scaled,
    ytrain_delta_scaled,
    validation_split=0.1,
    epochs=100,
    batch_size=32,
    callbacks=[early_stop],
    verbose=1
)

lstm_delta_pred_scaled = lstm_model.predict(Xtest_scaled).ravel()
lstm_delta_pred = y_scaler.inverse_transform(lstm_delta_pred_scaled.reshape(-1, 1)).ravel()
lstm_temp_pred = last_temp_test + lstm_delta_pred

lstm_mae, lstm_rmse, lstm_r2 = regression_metrics(future_temp_test, lstm_temp_pred)

print("LSTM (delta)")
print("MAE :", lstm_mae)
print("RMSE:", lstm_rmse)
print("R2  :", lstm_r2)

