print(df.dtypes)

df["delta_min"] = df["time"].diff().dt.total_seconds() / 60

print(df[["time", "delta_min"]].head(10))

print("\nDistribuzione dei delta temporali:")
print(df["delta_min"].value_counts(dropna=False).sort_index())

allowed_steps = {5, 10, 15}

df["delta_min_rounded"] = df["delta_min"].round()

# la prima riga non deve creare un gap artificiale
df["delta_min_rounded"] = df["delta_min_rounded"].fillna(5)

# is_gap = True solo se il salto NON è tra quelli ammessi
df["is_gap"] = ~df["delta_min_rounded"].isin(allowed_steps)

print(df[["time", "delta_min", "delta_min_rounded", "is_gap"]].head(15))

df["segmentid"] = df["is_gap"].cumsum()

segmentsizes = (
    df.groupby("segmentid")
      .size()
      .reset_index(name="length")
      .sort_values("length", ascending=False)
      .reset_index(drop=True)
)

print(segmentsizes.head(20))
print("\nNumero segmenti totali:", df["segmentid"].nunique())

windowsize = 30          # 30 misure passate
horizon = 6              # 6 step * 5 minuti = 30 minuti dopo
minlen = windowsize + horizon

validsegments = segmentsizes.loc[segmentsizes["length"] >= minlen, "segmentid"]

dfvalid = df[df["segmentid"].isin(validsegments)].copy().reset_index(drop=True)

print("Shape dfvalid:", dfvalid.shape)
print("Numero segmenti validi:", dfvalid["segmentid"].nunique())
print("Lunghezza minima richiesta:", minlen)

featurecols = ["temp", "hum", "lux"]
targetcol = "temp"

X, y, metatime = [], [], []

for segid, segdf in dfvalid.groupby("segmentid"):
    segdf = segdf.sort_values("time").reset_index(drop=True)

    values = segdf[featurecols].values
    target = segdf[targetcol].values
    times = segdf["time"].values

    for i in range(len(segdf) - windowsize - horizon + 1):
        X.append(values[i:i + windowsize])
        y.append(target[i + windowsize + horizon - 1])
        metatime.append(times[i + windowsize + horizon - 1])

X = np.array(X)
y = np.array(y)
metatime = np.array(metatime)

print("Shape di X:", X.shape)
print("Shape di y:", y.shape)
print("Prime date target:", metatime[:5])

Xflat = X.reshape(X.shape[0], X.shape[1] * X.shape[2])

print("Shape di Xflat:", Xflat.shape)

splitidx = int(len(Xflat) * 0.8)

Xtrain = Xflat[:splitidx]
