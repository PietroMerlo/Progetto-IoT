import tensorflow as tf
from tensorflow.keras.models import Sequential
from tensorflow.keras.layers import Input, LSTM, Dense, Dropout
from tensorflow.keras.callbacks import EarlyStopping

splitidx_lstm = int(len(X) * 0.8)

Xtrain_lstm = X[:splitidx_lstm]
Xtest_lstm = X[splitidx_lstm:]

ytrain_lstm = y[:splitidx_lstm]
ytest_lstm = y[splitidx_lstm:]

print("Train LSTM:", Xtrain_lstm.shape, ytrain_lstm.shape)
print("Test LSTM :", Xtest_lstm.shape, ytest_lstm.shape)

n_features = Xtrain_lstm.shape[2]

x_scaler = StandardScaler()
Xtrain_2d = Xtrain_lstm.reshape(-1, n_features)
Xtest_2d = Xtest_lstm.reshape(-1, n_features)

Xtrain_scaled = x_scaler.fit_transform(Xtrain_2d).reshape(Xtrain_lstm.shape)
Xtest_scaled = x_scaler.transform(Xtest_2d).reshape(Xtest_lstm.shape)

y_scaler = StandardScaler()
ytrain_scaled = y_scaler.fit_transform(ytrain_lstm.reshape(-1, 1)).ravel()
ytest_scaled = y_scaler.transform(ytest_lstm.reshape(-1, 1)).ravel()

tf.random.set_seed(42)
np.random.seed(42)

lstm_model = Sequential([
    Input(shape=(Xtrain_scaled.shape[1], Xtrain_scaled.shape[2])),
    LSTM(64),
    Dropout(0.2),
    Dense(32, activation="relu"),
    Dense(1)
])

lstm_model.compile(
    optimizer="adam",
    loss="mse",
    metrics=["mae"]
)

early_stop = EarlyStopping(
    monitor="val_loss",
    patience=15,
    restore_best_weights=True
)

history = lstm_model.fit(
    Xtrain_scaled,
    ytrain_scaled,
    validation_split=0.1,
    epochs=100,
    batch_size=32,
    callbacks=[early_stop],
    verbose=1
)

lstm_preds_scaled = lstm_model.predict(Xtest_scaled).ravel()
lstm_preds = y_scaler.inverse_transform(lstm_preds_scaled.reshape(-1, 1)).ravel()

lstm_mae, lstm_rmse, lstm_r2 = regression_metrics(ytest_lstm, lstm_preds)

print("LSTM")
print("MAE :", lstm_mae)
print("RMSE:", lstm_rmse)
print("R2  :", lstm_r2)

